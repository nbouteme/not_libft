#include <libft.h>

int     ft_toupper(int c)
{
	if(ft_isupper(c))
		c += 'a' - 'A';
	return c;
}

int     ft_tolower(int c)
{
	if(ft_islower(c))
		c -= 'a' - 'A';
	return c;
}
